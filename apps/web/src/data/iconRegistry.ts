/** Retrieve an icon component by its registry key, or undefined if not found. */
export function getRegistryIcon(key: string | undefined | null): ElementType | undefined {
  if (!key) return undefined;
  return iconRegistry[key];
}

/** Sorted entries for the icon picker UI. */
export const iconRegistryEntries: Array<{ key: string; icon: ElementType }> = Object.entries(
  iconRegistry,
)
  .map(([key, icon]) => ({ key, icon }))
  .sort((a, b) => a.key.localeCompare(b.key));
